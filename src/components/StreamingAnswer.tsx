'use client'

import { useEffect, useRef, useState } from 'react'
import { useStreamingAnswer, StreamState } from '../hooks/useStreamingAnswer'

interface StreamingAnswerProps {
  query: string
  autoStart?: boolean
  className?: string
  onComplete?: (answer: string) => void
}

// Thinking animation component
function ThinkingIndicator() {
  const [visible, setVisible] = useState(false)
  
  // Perceptual Polish: Only show thinking after 150ms to avoid flicker on fast responses
  useEffect(() => {
    const timer = setTimeout(() => setVisible(true), 150)
    return () => clearTimeout(timer)
  }, [])

  if (!visible) return null

  return (
    <div className="flex items-center gap-3 text-zinc-500 dark:text-zinc-400">
      <div className="flex gap-1">
        <span className="animate-pulse delay-0">‚óè</span>
        <span className="animate-pulse delay-100">‚óè</span>
        <span className="animate-pulse delay-200">‚óè</span>
      </div>
      <span className="text-sm">Thinking...</span>
    </div>
  )
}

// Streaming cursor
function StreamingCursor() {
  return (
    <span className="inline-block w-2 h-4 ml-0.5 bg-blue-500 dark:bg-blue-400 animate-pulse rounded-sm align-middle" />
  )
}

// Tier badge colors
function getTierStyles(tier: string) {
  switch (tier) {
    case 'fast':
      return 'bg-emerald-100 text-emerald-700 dark:bg-emerald-900/30 dark:text-emerald-400'
    case 'balanced':
      return 'bg-blue-100 text-blue-700 dark:bg-blue-900/30 dark:text-blue-400'
    case 'heavy':
      return 'bg-purple-100 text-purple-700 dark:bg-purple-900/30 dark:text-purple-400'
    case 'code':
      return 'bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400'
    default:
      return 'bg-zinc-100 text-zinc-700 dark:bg-zinc-800 dark:text-zinc-400'
  }
}

// Tier emoji
function getTierEmoji(tier: string) {
  switch (tier) {
    case 'fast': return '‚ö°'
    case 'balanced': return 'üß†'
    case 'heavy': return 'üî¨'
    case 'code': return 'üíª'
    default: return '‚ú®'
  }
}

// Model attribution badge
function ModelBadge({ 
  modelHuman, 
  tier, 
  latencyMs, 
  attempts,
  cached = false
}: { 
  modelHuman: string
  tier: string
  latencyMs: number
  attempts: number
  cached?: boolean
}) {
  return (
    <div className="flex items-center gap-2 mt-4 pt-3 border-t border-zinc-200 dark:border-zinc-700 flex-wrap">
      {/* Cache badge */}
      {cached && (
        <span className="px-2 py-0.5 rounded-full text-xs font-medium bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400">
          üì¶ Cached
        </span>
      )}
      
      {/* Tier badge */}
      <span className={`px-2 py-0.5 rounded-full text-xs font-medium ${getTierStyles(tier)}`}>
        {getTierEmoji(tier)} {tier.charAt(0).toUpperCase() + tier.slice(1)}
      </span>
      
      {/* Model info */}
      <span className="text-xs text-zinc-500 dark:text-zinc-400">
        {cached ? 'Served from' : 'Generated by'}{' '}
        <span className="font-medium text-zinc-700 dark:text-zinc-300">{modelHuman}</span>
      </span>
      
      {/* Timing info */}
      <span className="text-xs text-zinc-400 dark:text-zinc-500">
        ‚Ä¢ {cached ? 'instant' : `${latencyMs}ms TTFT`}
        {!cached && attempts > 1 && ` ‚Ä¢ ${attempts} attempts`}
      </span>
    </div>
  )
}

export function StreamingAnswer({ 
  query, 
  autoStart = true, 
  className = '',
  onComplete 
}: StreamingAnswerProps) {
  const { 
    answer, 
    state, 
    metadata, 
    error, 
    startStream, 
    reset,
    isLoading 
  } = useStreamingAnswer()
  
  const containerRef = useRef<HTMLDivElement>(null)
  const lastStartedQuery = useRef<string | null>(null)
  const completed = useRef(false)
  const shouldAutoScroll = useRef(true)

  // Standard Streaming UX: Only auto-scroll if user is near the bottom
  useEffect(() => {
    const el = containerRef.current
    if (!el) return

    const onScroll = () => {
      // Threshold of 40px from bottom to consider "at bottom"
      shouldAutoScroll.current = el.scrollHeight - el.scrollTop - el.clientHeight < 40
    }

    el.addEventListener('scroll', onScroll)
    return () => el.removeEventListener('scroll', onScroll)
  }, [])

  // Consolidated Lifecycle Effect
  useEffect(() => {
    if (autoStart && query && lastStartedQuery.current !== query) {
      lastStartedQuery.current = query
      completed.current = false
      startStream(query)
    }
  }, [query, autoStart, startStream])

  // Lifecycle Callback Guard: Prevents double-fire if state re-paints
  useEffect(() => {
    if (state === 'done' && onComplete && answer && !completed.current) {
      completed.current = true
      onComplete(answer)
    }
  }, [state, answer, onComplete])

  // Intelligent Auto-scroll
  useEffect(() => {
    if (state === 'streaming' && containerRef.current && shouldAutoScroll.current) {
      containerRef.current.scrollTop = containerRef.current.scrollHeight
    }
  }, [answer, state])

  const renderContent = () => {
    switch (state) {
      case 'idle':
        return <ThinkingIndicator />
        
      case 'thinking':
        return <ThinkingIndicator />
        
      case 'streaming':
        return (
          <div className="w-full max-w-none">
            <div className="whitespace-pre-wrap text-zinc-800 dark:text-zinc-200">
              {answer}
              {/* Avoid cursor drop if ending with newline */}
              {!answer.endsWith('\n') && <StreamingCursor />}
            </div>
          </div>
        )
        
      case 'done':
        return (
          <div className="w-full max-w-none animate-in fade-in duration-500">
            <div 
              className="text-[17px] leading-relaxed text-zinc-800 dark:text-zinc-200 font-sans space-y-4 min-h-[40px]"
              dangerouslySetInnerHTML={{ 
                __html: formatMarkdown(answer) || '<p class="text-zinc-400 italic">No content generated.</p>' 
              }}
            />
            {metadata && (
              <ModelBadge 
                modelHuman={metadata.modelHuman}
                tier={metadata.tier}
                latencyMs={metadata.latencyMs}
                attempts={metadata.attempts}
                cached={metadata.cached}
              />
            )}
          </div>
        )
        
      case 'error':
        return (
          <div className="flex items-center gap-2 text-red-500 dark:text-red-400">
            <span>‚ö†Ô∏è</span>
            <span className="text-sm">{error || 'An error occurred'}</span>
            <button 
              onClick={() => {
                completed.current = false
                startStream(query)
              }}
              className="ml-2 px-2 py-1 text-xs bg-red-100 dark:bg-red-900/30 rounded hover:bg-red-200 dark:hover:bg-red-900/50 transition-colors"
            >
              Retry
            </button>
          </div>
        )
        
      default:
        return null
    }
  }

  return (
    <div 
      ref={containerRef}
      className={`relative ${className}`}
    >
      {renderContent()}
      
      {/* Loading bar indicator */}
      {isLoading && (
        <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-zinc-200 dark:bg-zinc-700 overflow-hidden rounded-full">
          <div className="h-full w-full bg-gradient-to-r from-blue-500/0 via-blue-500 to-blue-500/0 animate-shimmer" />
        </div>
      )}
    </div>
  )
}

function escapeHTML(str: string) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
}

// Semantic Markdown Formatter
function formatMarkdown(text: string): string {
  let formatted = escapeHTML(text)

  // 1. Code Blocks (Preserve whitespace, dark mode contrast)
  formatted = formatted.replace(/```(\w*)\n?([\s\S]*?)```/g, (_, lang, code) => {
    return `<div class="my-6 rounded-xl overflow-hidden bg-[#0d0d0d] border border-white/10 shadow-lg"><div class="px-4 py-2 bg-white/5 border-b border-white/5 text-[11px] font-mono text-zinc-400 flex justify-between uppercase tracking-wider"><span>${lang || 'CODE'}</span></div><div class="p-4 overflow-x-auto"><pre><code class="font-mono text-sm text-zinc-300 leading-relaxed">${code.trim()}</code></pre></div></div>`
  })

  // 2. Headers (Apple-style hierarchy)
  formatted = formatted.replace(/^# (.*$)/gm, '<h1 class="text-2xl font-semibold mt-8 mb-4 text-zinc-900 dark:text-zinc-50 tracking-tight">$1</h1>')
  formatted = formatted.replace(/^## (.*$)/gm, '<h2 class="text-xl font-semibold mt-8 mb-3 text-zinc-900 dark:text-zinc-100 tracking-tight">$1</h2>')
  formatted = formatted.replace(/^### (.*$)/gm, '<h3 class="text-lg font-medium mt-6 mb-3 text-zinc-800 dark:text-zinc-200">$1</h3>')

  // 3. Bold & Italic
  formatted = formatted.replace(/\*\*(.*?)\*\*/g, '<strong class="font-semibold text-zinc-900 dark:text-zinc-50">$1</strong>')
  formatted = formatted.replace(/\*(.*?)\*/g, '<em class="italic text-zinc-600 dark:text-zinc-400">$1</em>')

  // 4. Inline Code
  formatted = formatted.replace(/`([^`]+)`/g, '<code class="px-1.5 py-0.5 bg-zinc-100 dark:bg-white/10 rounded-[6px] font-mono text-[13px] text-pink-600 dark:text-pink-400 border border-black/5 dark:border-white/5">$1</code>')

  // 5. Lists (Unordered - Bullet points)
  formatted = formatted.replace(/^\s*-\s+(.*)$/gm, '<div class="flex items-start gap-3 mb-3 ml-1 group"><span class="mt-2 w-1.5 h-1.5 rounded-full bg-zinc-300 dark:bg-zinc-600 shrink-0 group-hover:bg-blue-500 transition-colors"></span><span class="text-[17px] leading-relaxed text-zinc-700 dark:text-zinc-300">$1</span></div>')
  
  // 6. Lists (Ordered)
  formatted = formatted.replace(/^\s*(\d+)\.\s+(.*)$/gm, '<div class="flex items-start gap-3 mb-3 ml-1"><span class="text-sm font-mono text-zinc-400 dark:text-zinc-500 mt-1 select-none">$1.</span><span class="text-[17px] leading-relaxed text-zinc-700 dark:text-zinc-300">$2</span></div>')

  // 7. Citations (Superscript interactive)
  formatted = formatted.replace(/\[(\d+)\]/g, '<sup class="ml-0.5 text-[10px] font-bold text-blue-600 dark:text-blue-400 bg-blue-50 dark:bg-blue-500/10 px-1.5 py-0.5 rounded cursor-pointer hover:bg-blue-100 dark:hover:bg-blue-500/20 transition-colors select-none">[$1]</sup>')

  // 8. Paragraphs (Detect Double Newline)
  // We first replace \n\n with a spacer div
  formatted = formatted.replace(/\n\n/g, '<div class="h-6"></div>')
  // Then single newlines with spaces to allow text flowing, OR <br> if you prefer strict breaks.
  // Standard Markdown usually ignores single newlines, but for AI chat, we often want to preserve them as soft breaks or spaces.
  // Let's treat valid text blocks as paragraphs implicitly by the container's spacing, 
  // but explicitly handle breaks that weren't double newlines.
  formatted = formatted.replace(/([^\n])\n([^\n])/g, '$1<br class="md:hidden" /> $2') 

  return formatted
}

